# Polimofirsmo #

# A palavra "polimorfismo" significa "muitas formas", e na programação refere-se a métodos/funções/operadores #2 com o mesmo nome que podem ser executados em muitos objetos ou classes. #

print(" - Polimorfismo: \n")

# a função 'len()' pode ser usada em diferentes objetos

print("Função 'len()' para string: \n")

x = "Olá Mundo!"
print(len(x),"\n")

#

print("Função 'len()' para tupla: \n")

fruitTuple = ("maçã", "banana", "cereja")
print(len(fruitTuple), "\n")

