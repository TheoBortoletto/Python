# Booleanos representam dois valores: 'True' ou 'False'

print(" - Exemplo de expressão booleana, checar se é veredadeiro ou falso com um 'if': \n")

a = 200
b = 199

if b > a:
    print(b, "é maior do que", a)

else:
    print(a, "é maior que", b, "\n")

