### Listas ###

randomList = [55, "Théo", "Larissa", "Alan", 87, "Lívia", "Beatriz"]
listNum = [1, 2, 3, 4, 5]

print("-------------------------------------------------------------- \n")
print("\t Listas: \n")
print(randomList)
print("\n")
print(listNum)
print("\n")
print("-------------------------------------------------------------- \n")

### Copiar Listas ###

print("\t Copiando uma lista usando o 'copy()': \n")
copyRandomList = randomList.copy()
print(copyRandomList)
print("\n")

print("\t Copiando uma lista usando o 'list()': \n")
copyListNum = list(listNum)
print(copyListNum)
print("\n")

print("-------------------------------------------------------------- \n")
